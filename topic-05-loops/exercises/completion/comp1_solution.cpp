// Completion Problem 1: Counting Loop - SOLUTION

#include <iostream>
using namespace std;

int main() {
    // Using a for loop
    for (int i = 1; i <= 10; i++) {
        cout << i << " ";
    }
    cout << '\n';

    return 0;
}

/*
Solution:
- Blank 1: 1 (start at 1)
- Blank 2: 10 (go up to 10)
- Blank 3: i++ (increment by 1)
*/
