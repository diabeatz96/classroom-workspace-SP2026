#include <iostream>
using namespace std;

int main() {
    // Display the top border
    cout << "+----------------------+" << '\n';

    // Display the name line
    cout << "|  John Smith          |" << '\n';

    // Display the title line with a newline inside the string
    cout << "|  Software Developer  |\n";

    // Display a blank line in the card
    cout << "|                      |" << '\n';

    // Display contact info with a tab for spacing
    cout << "|  Phone: 555-1234     |" << '\n';

    // Display the bottom border
    cout << "+----------------------+" << '\n';

    return 0;
}

/*
Solution Explanation:
- Blank 1: #include - preprocessor directive to include libraries
- Blank 2: namespace - the keyword before std
- Blank 3: int - the return type of main()
- Blank 4: << - the insertion operator
- Blank 5: << - chains another output item
- Blank 6: cout - the standard output object
- Blank 7: return - sends a value back from the function
*/
