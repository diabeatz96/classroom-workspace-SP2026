// Completion Problem 1: Greeting Message - SOLUTION

#include <iostream>
using namespace std;

int main() {
    // Display "Hello, there!" followed by a new line
    cout << "Hello, there!" << '\n';

    // Display "Welcome to C++!" followed by a new line
    cout << "Welcome to C++!" << '\n';

    // Return 0 to indicate success
    return 0;
}

/*
Solution Explanation:
- Blank 1: cout - the standard output stream object
- Blank 2: << - the insertion operator that sends data to cout
- Blank 3: 0 - indicates successful program completion
*/
