#include <iostream>
#include <string>
using namespace std;

int main() {
    // Declare an integer variable named 'age' with value 21
    int age = 21;

    // Declare a double variable named 'gpa' with value 3.5
    double gpa = 3.5;

    // Declare a character variable named 'grade' with value 'B'
    char grade = 'B';

    // Declare a string variable named 'name' with value "Sam"
    string name = "Sam";

    // Declare a boolean variable named 'isEnrolled' with value true
    bool isEnrolled = true;

    // Display all variables
    cout << "Name: " << name << '\n';
    cout << "Age: " << age << '\n';
    cout << "GPA: " << gpa << '\n';
    cout << "Grade: " << grade << '\n';
    cout << "Enrolled: " << isEnrolled << '\n';

    return 0;
}

