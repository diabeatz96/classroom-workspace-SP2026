// Completion Problem 1: Basic Calculations - SOLUTION

#include <iostream>
using namespace std;

int main() {
    int x = 10;
    int y = 3;

    // Calculate the sum of x and y
    int sum = x + y;

    // Calculate x divided by y (integer division)
    int quotient = x / y;

    // Calculate the remainder when x is divided by y
    int remainder = x % y;

    // Display results
    cout << "x = " << x << ", y = " << y << '\n';
    cout << "Sum: " << sum << '\n';
    cout << "Quotient: " << quotient << '\n';
    cout << "Remainder: " << remainder << '\n';

    return 0;
}

/*
Solution:
- Blank 1: + (addition operator)
- Blank 2: / (division operator)
- Blank 3: % (modulo operator)
- Blank 4: sum
- Blank 5: remainder
*/
